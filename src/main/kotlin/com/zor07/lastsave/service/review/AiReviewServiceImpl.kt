package com.zor07.lastsave.service.review

import org.springframework.stereotype.Service

@Service
class AiReviewServiceImpl(
    private val openAiClient: OpenAiClient,
) : AiReviewService {

    override fun review(diff: String): String {
        val prompt = """
            Ты — строгий ревьюер кода на стажировке по Java. Твоя задача — дать короткую и полезную обратную связь.

            Правила:
            - Пиши только о проблемах и реальных улучшениях. Не хвали за очевидные вещи (сложение, деление, простые условия).
            - Максимум 5 пунктов. Если проблем нет — 1-2 предложения и всё.
            - Не пересказывай что делает код — студент это знает.
            - Не переписывай код за студента, направляй вопросами или подсказками.
            - Студент решает задачи в заданных рамках: сигнатура метода — это условие задачи, менять её нельзя. Не советуй использовать другие типы параметров, сторонние классы или библиотеки — это нерелевантно.
            - В конце всегда итог по формуле: короткая искренняя похвала (не "молодец", каждый раз по-разному) + можно мержить + замечания учесть в будущем или для расширения кругозора. Всё одним-двумя предложениями. Рекомендацию исправить давай только если есть явный баг или принципиально неверная логика.
            - Пиши по-русски, коротко и по делу. Без воды и похвал за базовые вещи.
            - Ответ не должен превышать 1000 символов.

            Код студента (diff):
            $diff
        """.trimIndent()

        return openAiClient.complete(prompt)
    }
}
